package part2.part7;

/*
Демонстрация динамической инициализации.
 */
public class Part7 {
    public static void main(String args[]) {
        double radius = 4.0;
        double height = 5.0;

        double volume = Math.PI * Math.pow(radius, 2) * height;

        System.out.println("Объем: " + volume);

    }
}